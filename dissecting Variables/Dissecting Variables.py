
user_input = input("Type in something: ")

print("\nAre there only spaces?", user_input.isspace())
print("Is it a number?", user_input.isdigit())
print("Is it alphabetical?", user_input.isalpha())
print("Is it alphanumeric?", user_input.isalnum())
print("Is it all uppercase?", user_input.isupper())
print("Is it all lower case?", user_input.islower())
print("Is it capital case?", user_input.istitle())
